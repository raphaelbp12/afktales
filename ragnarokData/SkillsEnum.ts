export function skillStringToEnum(skillString: string): e_skill | null {
  if (
    typeof skillString === "string" &&
    isNaN(Number(skillString)) && // Ensures that the string is not a numeric value
    skillString in e_skill
  ) {
    return e_skill[skillString as keyof typeof e_skill];
  } else {
    return null;
  }
}
